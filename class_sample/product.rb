class Product
  # クラスインスタンス変数
  @name = 'Product'

  def self.name
    # クラスインスタンス変数
    @name
  end

  def initialize(name)
    # インスタンス変数
    @name = name
  end

  def name
    # インスタンス変数
    @name
  end

  def hello
    'Hello'
  end

  alias greeting hello
end

user = Product.new
user.hello
user.greeting
